    if(pwork=='contents')document.getElementById("wpahr").innerHTML="내가 박준현이다!";
    else if(pwork=='attr')document.getElementById("qnwpahr").style="color: green";
    else if(pwork=='change_img')document.getElementById("imgsss").src="http://arabbit.link/images/tiger.png";
